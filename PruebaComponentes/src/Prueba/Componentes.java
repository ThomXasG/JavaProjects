    public void Cargar(File selectedFile) {
        DefaultTableModel model = new DefaultTableModel();
        tblTabla.setModel(model);
        try (FileInputStream fis = new FileInputStream(selectedFile);
            Workbook workbook = new HSSFWorkbook(fis)) {

            Sheet sheet = workbook.getSheetAt(0); // Hoja de trabajo (puedes cambiar el índice si hay varias hojas)

            int rowCount = sheet.getPhysicalNumberOfRows();

            if (rowCount > 0) {
                Row headerRow = sheet.getRow(0); // Encabezados
                int columnCount = headerRow.getPhysicalNumberOfCells();

                for (int col = 0; col < columnCount; col++) {
                    model.addColumn(headerRow.getCell(col).toString());
                }

                for (int row = 1; row < rowCount; row++) {
                    Row dataRow = sheet.getRow(row);
                    Object[] rowData = new Object[columnCount];

                    for (int col = 0; col < columnCount; col++) {
                        Cell cell = dataRow.getCell(col);
                        rowData[col] = cell.toString();
                    }

                    model.addRow(rowData);
                }
            }
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar el archivo de Excel.");
        }
    }

