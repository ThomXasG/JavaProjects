    private void vacaTextField2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_vacaTextField2FocusLost
        // TODO add your handling code here:
        String text = vacaTextField2.getText();
        
        try {
            // Parse the text as a double with two decimal places
            double value = Double.parseDouble(text);
            DecimalFormat decimalFormat = new DecimalFormat("0.00");
            String formattedValue = decimalFormat.format(value);
            vacaTextField2.setText(formattedValue);
        } catch (NumberFormatException ex) {
            // Handle invalid input here, e.g., display an error message
            vacaTextField2.setText("0.00");
        }
    }//GEN-LAST:event_vacaTextField2FocusLost

    private void vacaBoton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vacaBoton1ActionPerformed
        // TODO add your handling code here:
        java.io.File selectedFile;
                
        JFileChooser fileChooser = new JFileChooser();

        javax.swing.filechooser.FileFilter excelFilter = new javax.swing.filechooser.FileFilter() {
            public boolean accept(File file) {
                String fileName = file.getName().toLowerCase();
                return fileName.endsWith(".xlsx") || fileName.endsWith(".xls") || file.isDirectory();
            }

            public String getDescription() {
                return "Archivos de Excel (*.xlsx, *.xls)";
            }
        };
        
        fileChooser.setFileFilter(excelFilter);
        
        int result = fileChooser.showOpenDialog(this);
        
        if (result == JFileChooser.APPROVE_OPTION) {
            // Aquí puedes manejar el archivo seleccionado
            selectedFile = fileChooser.getSelectedFile();
            JOptionPane.showMessageDialog(this, "Archivo seleccionado");
            
        }
        
        
    }//GEN-LAST:event_vacaBoton1ActionPerformed

