    private void llenarTabla(List<Estudiante> listaEstudiantes) {
        
        modelo = (DefaultTableModel) Tabla.getModel();
        modelo.setRowCount(0);
        for (Estudiante estudiante : listaEstudiantes) {
            // Agregar una fila a la tabla con los datos de la persona
            modelo.addRow(new Object[]{estudiante.getCedula(), estudiante.getNombre(), estudiante.getApellido(), estudiante.getDirreccion(), estudiante.getTelefono()});
        }
    }

    public void cargarTextos() {
        Tabla.getSelectionModel().addListSelectionListener((ListSelectionEvent e) -> {
            if (Tabla.getSelectedRow() != -1) {
                int fila = Tabla.getSelectedRow();
                jtxtCedula.setText(Tabla.getValueAt(fila, 0).toString());
                jtxtNombre.setText(Tabla.getValueAt(fila, 1).toString());
                jtxtApellido.setText(Tabla.getValueAt(fila, 2).toString());
                jtxtDireccion.setText(Tabla.getValueAt(fila, 3).toString());
                jtxtTelefono.setText(Tabla.getValueAt(fila, 4).toString());
            }
        });
    }

